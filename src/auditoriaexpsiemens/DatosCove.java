public class DatosCove {
    
    private int idCove = -1;
    private String idCoveVucem;
    private String edocum;
    private String referencia;

    /**
     * @return the idCove
     */
    public int getIdCove() {
        return idCove;
    }

    /**
     * @param idCove the idCove to set
     */
    public void setIdCove(int idCove) {
        this.idCove = idCove;
    }

    /**
     * @return the edocum
     */
    public String getEdocum() {
        return edocum;
    }

    /**
     * @param edocum the edocum to set
     */
    public void setEdocum(String edocum) {
        this.edocum = edocum;
    }

    /**
     * @return the idCoveVucem
     */
    public String getIdCoveVucem() {
        return idCoveVucem;
    }

    /**
     * @param idCoveVucem the idCoveVucem to set
     */
    public void setIdCoveVucem(String idCoveVucem) {
        this.idCoveVucem = idCoveVucem;
    }

    /**
     * @return the referencia
     */
    public String getReferencia() {
        return referencia;
    }

    /**
     * @param referencia the referencia to set
     */
    public void setReferencia(String referencia) {
        this.referencia = referencia;
    }
}
